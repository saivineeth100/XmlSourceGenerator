using BenchmarkDotNet.Attributes;
using BenchmarkDotNet.Running;
using XmlSourceGenerator;
using XmlSourceGenerator.Abstractions;
using System.Collections.Generic;
using System.Xml.Linq;

namespace XmlSourceGenerator.Benchmarks
{
    [XmlAutoGenerated]
    public partial class BenchmarkEntity
    {
        public int Id { get; set; }
        public string Name { get; set; }
        public string Description { get; set; }
        public double Value { get; set; }
        public bool IsActive { get; set; }
    }

    [MemoryDiagnoser]
    [ThreadingDiagnoser]
    public class GenericXmlStreamerBenchmarks
    {
        private List<BenchmarkEntity> _smallCollection;
        private List<BenchmarkEntity> _largeCollection;
        private List<BenchmarkEntity> _hugeCollection;
        private XElement _smallXml;
        private XElement _largeXml;
        private XElement _hugeXml;

        [GlobalSetup]
        public void Setup()
        {
            // Small: 10 items
            _smallCollection = CreateEntities(10);
            _smallXml = CreateXml(_smallCollection);

            // Large: 1,000 items
            _largeCollection = CreateEntities(1000);
            _largeXml = CreateXml(_largeCollection);

            // Huge: 10,000 items
            _hugeCollection = CreateEntities(10000);
            _hugeXml = CreateXml(_hugeCollection);
        }

        private List<BenchmarkEntity> CreateEntities(int count)
        {
            var list = new List<BenchmarkEntity>(count);
            for (int i = 0; i < count; i++)
            {
                list.Add(new BenchmarkEntity
                {
                    Id = i,
                    Name = $"Entity_{i}",
                    Description = $"Description for entity {i}",
                    Value = i * 1.5,
                    IsActive = i % 2 == 0
                });
            }
            return list;
        }

        private XElement CreateXml(List<BenchmarkEntity> entities)
        {
            var root = new XElement("Root");
            foreach (var entity in entities)
            {
                root.Add(entity.WriteToXml());
            }
            return root;
        }

        #region Serialization Benchmarks

        [Benchmark]
        public XElement Serialize_Small_10Items()
        {
            var root = new XElement("Root");
            foreach (var entity in _smallCollection)
            {
                root.Add(entity.WriteToXml());
            }
            return root;
        }

        [Benchmark]
        public XElement Serialize_Large_1000Items()
        {
            var root = new XElement("Root");
            foreach (var entity in _largeCollection)
            {
                root.Add(entity.WriteToXml());
            }
            return root;
        }

        [Benchmark]
        public XElement Serialize_Huge_10000Items()
        {
            var root = new XElement("Root");
            foreach (var entity in _hugeCollection)
            {
                root.Add(entity.WriteToXml());
            }
            return root;
        }

        #endregion

        #region Deserialization Benchmarks

        [Benchmark]
        public List<BenchmarkEntity> Deserialize_Small_10Items()
        {
            var result = new List<BenchmarkEntity>();
            foreach (var element in _smallXml.Elements())
            {
                var entity = new BenchmarkEntity();
                entity.ReadFromXml(element);
                result.Add(entity);
            }
            return result;
        }

        [Benchmark]
        public List<BenchmarkEntity> Deserialize_Large_1000Items()
        {
            var result = new List<BenchmarkEntity>();
            foreach (var element in _largeXml.Elements())
            {
                var entity = new BenchmarkEntity();
                entity.ReadFromXml(element);
                result.Add(entity);
            }
            return result;
        }

        [Benchmark]
        public List<BenchmarkEntity> Deserialize_Huge_10000Items()
        {
            var result = new List<BenchmarkEntity>();
            foreach (var element in _hugeXml.Elements())
            {
                var entity = new BenchmarkEntity();
                entity.ReadFromXml(element);
                result.Add(entity);
            }
            return result;
        }

        #endregion

        #region Round-Trip Benchmarks

        [Benchmark]
        public List<BenchmarkEntity> RoundTrip_Large_1000Items()
        {
            // Serialize
            var root = new XElement("Root");
            foreach (var entity in _largeCollection)
            {
                root.Add(entity.WriteToXml());
            }

            // Deserialize
            var result = new List<BenchmarkEntity>();
            foreach (var element in root.Elements())
            {
                var entity = new BenchmarkEntity();
                entity.ReadFromXml(element);
                result.Add(entity);
            }
            return result;
        }

        #endregion
    }

    public class Program
    {
        public static void Main(string[] args)
        {
            var summary = BenchmarkRunner.Run<GenericXmlStreamerBenchmarks>();
        }
    }
}
